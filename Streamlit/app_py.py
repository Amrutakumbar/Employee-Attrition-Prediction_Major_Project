import streamlit as st
import pandas as pd
import joblib

# ===========================
# Load Model
# ===========================

model = joblib.load("employee_attrition_model.pkl")
scaler = joblib.load("scaler.pkl")

# ===========================
# Load Dataset
# ===========================

df = pd.read_csv("WA_Fn-UseC_-HR-Employee-Attrition.csv")

# Remove target column
X = df.drop("Attrition", axis=1)

# Encode categorical columns
for col in X.select_dtypes(include="object").columns:
    X[col] = X[col].astype("category").cat.codes

feature_names = X.columns

# ===========================
# Page Config
# ===========================

st.set_page_config(
    page_title="Employee Attrition Prediction",
    page_icon="📊",
    layout="wide"
)

st.title("📊 Employee Attrition Prediction System")

st.write("Predict whether an employee is likely to leave the company.")

st.markdown("---")

# ===========================
# Input Fields
# ===========================

user_input = []

for col in feature_names:

    value = st.number_input(
        col,
        value=float(X[col].mean())
    )

    user_input.append(value)

# ===========================
# Prediction
# ===========================

if st.button("Predict"):

    input_df = pd.DataFrame([user_input], columns=feature_names)

    input_scaled = scaler.transform(input_df)

    prediction = model.predict(input_scaled)

    probability = model.predict_proba(input_scaled)

    leave = probability[0][1] * 100

    stay = probability[0][0] * 100

    st.markdown("---")

    if prediction[0] == 1:

        st.error("⚠ Employee is likely to leave.")

    else:

        st.success("✅ Employee is likely to stay.")

    st.write(f"**Stay Probability:** {stay:.2f}%")

    st.write(f"**Leave Probability:** {leave:.2f}%")

    if leave < 30:

        st.success("Risk Level : LOW")

    elif leave < 60:

        st.warning("Risk Level : MEDIUM")

    else:

        st.error("Risk Level : HIGH")

